Recommendation
Use W3Schools to move fast through the basics, but pair it with deeper resources for the systems-level parts:

learncpp.com — free, far more thorough, covers memory, smart pointers, move semantics, templates properly
cppreference.com — the reference you'll live in
A Tour of C++ (Stroustrup) or Effective Modern C++ (Meyers) for modern practices
